def is_valid_preset_name(preset_name: str) -> bool:
    return preset_name in get_preset_names()


def build_preset_combo_items(combo_widget) -> None:
    combo_widget.blockSignals(True)
    combo_widget.clear()
    combo_widget.addItem(get_preset_placeholder_label())
    combo_widget.insertSeparator(combo_widget.count())
    for preset_name in get_preset_names():
        combo_widget.addItem(preset_name)
    combo_widget.blockSignals(False)
    return None


def pre_block_signals(widget_collection: dict) -> None:
    process_profile_widgets_block_signals(widget_collection, True)
    return None


def post_unblock_signals(widget_collection: dict) -> None:
    process_profile_widgets_block_signals(widget_collection, False)
    return None


def reset_all_profile_widgets(widget_collection: dict) -> None:
    process_profile_widgets_reset(widget_collection)
    return None


def process_preset_apply(widget_collection: dict, preset_name: str) -> bool:
    if not is_valid_preset_name(preset_name): return False
    pre_block_signals(widget_collection)
    reset_all_profile_widgets(widget_collection)
    for tab_name, tab_settings in get_preset_by_name(preset_name).items():
        for setting_key, setting_value in tab_settings.items():
            widget_key = tab_name + ":" + setting_key
            if widget_collection.get(widget_key) is None: continue
            process_widget_value_update(widget_collection[widget_key], setting_value)
    post_unblock_signals(widget_collection)
    return True
